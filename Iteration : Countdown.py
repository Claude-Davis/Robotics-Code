# take a number as input
number = int(input())

#use a 'while loop' for the countdown
while number >= 0:             #while the number is greater than or equal to zero (0)...
    print(number)              #the value of the variable 'number' will be printed.
    number = number - 1        #after each display of the value for 'number,' the value is reduced by 1.
